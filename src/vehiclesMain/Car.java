package vehiclesMain;

public class Car {
	
	public String name;
	
	public int mileage;
	
	public int year;
	
	public boolean convertible;
	
	public String fuelage;
	
	public Car (String name, int mileage, int year, String fuelage) {
		this.name = name;
		this.mileage = mileage;
		this.year = year;
		this.convertible = convertible;
		this.fuelage = fuelage;
	}
	
}
